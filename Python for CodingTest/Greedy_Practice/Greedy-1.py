# 그리디 알고리즘

"""

정의 : 현재 상황에서 지금 당장 좋은 것만 고르는 방법

==>'가장 큰 순서대로', '가장 작은 순서대로'와 같은 기준 찾기

예제1 거스름돈(대표적인 그리디 알고리즘 문제)

당신은 음식점의 계산을 도와주는 점원이다. 카운터에는 거스름돈으로 사용할
500원, 100원, 50원, 10원짜리 동전이 무한히 존재한다고 가정한다. 손님에게
거슬러 줘야할 돈이 N원일 때 거슬러 줘야할 동전의 최소 개수를 구하라.
단, 거슬러 줘야 할 돈 N은 항상 10의 배수이다.

"""

# 예제1 풀이

n = 1260
count = 0

# 큰 단위의 화폐부터 차례대로 확인

list = [500, 100, 50, 10]

for coin in list:
    count += n // coin  # 해당 화폐로 거슬러 줄 수 있는 동전의 개수 세기
    n %= coin

print(count)
