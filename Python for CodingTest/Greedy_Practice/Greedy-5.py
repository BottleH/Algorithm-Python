# 그리디 알고리즘

"""

실전문제 1 - 1이 될 때까지
어떠한 수 N이 1이 될 때까지 다음의 두 과정 중 하나를 반복적으로 선택하여 수행하려고 한다. 단, 두번째 연산은
N이 K로 나누어떨어질 때만 선택할 수 있다.
1. N에서 1을 뺀다.
2. N을 K로 나눈다.

N과 K가 주어질 때 N이 1이 될 때까지 1번 혹은 2번의 과정을 수행해야 하는 최소 횟수를 구하는 프로그램을 작성하시오.

"""

n, k = map(int, input().split())
result = 0  # 횟수 계산할 변수

# n이 1이 되기전까지 계속 나누기
while n != 1:
    if n % k == 0:
        n /= k
        result += 1
    else:
        n = n - 1
        result += 1
print(result)
